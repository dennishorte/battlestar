#!/usr/bin/env node
'use strict'

// Walks the workspace, finds every index.js carrying the marker line, and
// rewrites it as a sorted list of `require('./<file>.js')` calls plus a
// `module.exports = [...]` array.
//
// Why a build-time codegen instead of a runtime helper: card-index files
// are bundled into the browser via Vite, and `fs.readdirSync` / `__dirname`
// don't exist there. Producing static index.js at commit time gives the
// "no manual editing when adding a card" ergonomics universally — the
// committed file works in Node tests, build scripts, and the browser bundle.
//
// To opt a directory in, create an index.js whose first lines include the
// MARKER below. Then run `npm run regen-card-indexes -w common`.

const fs = require('fs')
const path = require('path')

const MARKER = '// AUTO-GENERATED by `npm run regen-card-indexes -w common`. Do not edit by hand.'
const ROOT = path.resolve(__dirname, '..')

let regenerated = 0

function isCardFile(name) {
  return name.endsWith('.js') && name !== 'index.js' && !name.endsWith('.test.js')
}

function varNameForFile(file) {
  // `acquire-the-spice-must-flow.js` → `acquire_the_spice_must_flow`
  // Non-alphanumeric characters become underscores; leading digits get
  // an underscore prefix to stay a valid identifier.
  let name = file.slice(0, -3).replace(/[^a-zA-Z0-9]/g, '_')
  if (/^[0-9]/.test(name)) {
    name = '_' + name
  }
  return name
}

function regenIndex(dir) {
  const files = fs.readdirSync(dir).filter(isCardFile).sort()

  const lines = ["'use strict'", '', MARKER, '']
  const exports = []

  for (const file of files) {
    const varName = varNameForFile(file)
    lines.push(`const ${varName} = require('./${file}')`)
    exports.push(varName)
  }

  lines.push('')
  lines.push('module.exports = [')
  for (const name of exports) {
    lines.push(`  ${name},`)
  }
  lines.push(']')

  fs.writeFileSync(path.join(dir, 'index.js'), lines.join('\n') + '\n')
  regenerated++
  console.log(`✓ ${path.relative(ROOT, path.join(dir, 'index.js'))}`)
}

function walk(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true })
  for (const entry of entries) {
    if (entry.name === 'node_modules' || entry.name.startsWith('.')) {
      continue
    }
    const fullPath = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      walk(fullPath)
    }
    else if (entry.name === 'index.js') {
      const content = fs.readFileSync(fullPath, 'utf8')
      if (content.includes(MARKER)) {
        regenIndex(dir)
      }
    }
  }
}

walk(ROOT)
console.log(`\n${regenerated} index file(s) regenerated`)
